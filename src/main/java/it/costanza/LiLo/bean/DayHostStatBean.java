package it.costanza.LiLo.bean;

public class DayHostStatBean {

	private int numDayHost;
	private int avgModulePerDay;
	private String arrayModuleTypeName;
	private String arrayDayHostComposition;
	
	public int getNumDayHost() {
		return numDayHost;
	}
	public void setNumDayHost(int numDayHost) {
		this.numDayHost = numDayHost;
	}
	public int getAvgModulePerDay() {
		return avgModulePerDay;
	}
	public void setAvgModulePerDay(int avgModulePerDay) {
		this.avgModulePerDay = avgModulePerDay;
	}
	public String getArrayModuleTypeName() {
		return arrayModuleTypeName;
	}
	public void setArrayModuleTypeName(String arrayModuleTypeName) {
		this.arrayModuleTypeName = arrayModuleTypeName;
	}
	public String getArrayDayHostComposition() {
		return arrayDayHostComposition;
	}
	public void setArrayDayHostComposition(String arrayDayHostComposition) {
		this.arrayDayHostComposition = arrayDayHostComposition;
	}
	
	
}
