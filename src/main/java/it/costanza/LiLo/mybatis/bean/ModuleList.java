package it.costanza.LiLo.mybatis.bean;

public class ModuleList {
    private Integer idModule;

    private Integer idElement;

    private Integer idListContent;

    public Integer getIdModule() {
        return idModule;
    }

    public void setIdModule(Integer idModule) {
        this.idModule = idModule;
    }

    public Integer getIdElement() {
        return idElement;
    }

    public void setIdElement(Integer idElement) {
        this.idElement = idElement;
    }

    public Integer getIdListContent() {
        return idListContent;
    }

    public void setIdListContent(Integer idListContent) {
        this.idListContent = idListContent;
    }
}